console.log("-----Swap Two Variables-----");
console.log("-----Before Swap-----");
var sweety = "sweety";
var cutie = "cutie";
console.log("Sweety:",sweety);
console.log("Cutie:",cutie);
console.log("-----After Swap-----");
var temp = sweety;
sweety = cutie;
cutie = temp;
console.log("Sweety:",sweety);
console.log("Cutie:",cutie);

